public class Colour {

    private int red;
    private int green;
    private int blue;

    public int getRed() {
        return red;
    }

    public void setRed(int r) {
        this.red = r;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int g) {
        this.green = g;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int b) {
        this.blue = b;
    }

}